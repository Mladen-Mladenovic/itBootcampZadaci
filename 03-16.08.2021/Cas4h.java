package primer02;

import java.util.Scanner;

public class Cas4h {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		System.out.println("Unesite broj puta za ispis -Laku noc!-");
		int i = scan.nextInt();
		String s = "";
		while (i > 0) {
			System.out.println(s + "Laku noc!");
			i--;
			
	}
}
}